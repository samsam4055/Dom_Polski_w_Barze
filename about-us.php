			<p> <img src="img/o-nas.jpg" width="540" height="340" alt="Dom Polski w Barze" class="dp-img"> Pomysłodawcą i inicjatorem powstania Domu Polskiego w Barze był były Konsul Generalny RP w Kijowie Radca – Minister Sylwester Szostak. To dzięki jego motywacji i wsparciu postanowiliśmy złożyć w 2004 roku do Stowarzyszenia „Wspólnota Polska” w Warszawie wniosek dotyczący budowy tego obiektu.</p>
			<p>Ostateczna decyzja o budowie Domu Polskiego w Barze została podjęta przez Zarząd Krajowy Stowarzyszenia „Wspólnota Polska”.</p>
			<p>W pażdzierniku 2007 roku odbyło się uroczyste położenie kamienia węgielnego pod budowę obiektu. Następnie, dzięki dużemu zaangażowaniu przede wszystkim naszych przyjaciół z Kwidzyna, było możliwe rozpoczęcie budowy tego dużego projektu. W póżniejszym okresie do wspierających nas przez cały niełatwy okres budowy nasze dążenia wspierał Konsulat RP w Winnicy.</p>
			<p class="p-fat">Utrzymanie siedziby Domu Polskiego w Barze jest finansowane przez <a href="http://www.wspolnota-polska.org.pl/" target="_blank">Stowarzyszenie „Wspólnota Polska”</a> ze środków otrzymanych z Kancelarii Senatu Rzeczypospolitej Polskiej w ramach sprawowania opieki Senatu RP nad Polonią i Polakami za granicą.<img src="img/dp-table.jpg" width="200" height="300" alt="Dom Polski w Barze" class="dp-table"></p>
			<p>Dom Polski w Barze stanowi doskonałą bazę lokalową dla nauczania języka polskiego.</p>
			<p>W środowisku istnieje bardzo duże zapotrzebowanie w tym zakresie, dlatego Dom Polski przygotował ofertę edukacyjną dla grup wiekowych począwszy od sześciolatków , a skończywszy na osobach dorosłych. W bieżącym roku działa dwadzieścia jeden grup językowych, w których uczy się około 220 uczniów na trzech poziomach, w zależności od wieku i stopnia zaawansowania. Zajęcia prowadzone są w godzinach popołudniowych i w miarę możliwości dostosowane do rozkładu lekcji w szkołach i godzin powrotu z pracy. Lekcje w postaci zajęć fakultatywnych i kursów językowych prowadzą dwie osoby: nauczyciel wolontariusz skierowany tu do pracy przez Ośrodek Rozwoju Polskiej Edukacji za Granicą – pani Małgorzata Michalska i miejscowy nauczyciel języka polskiego – pani Maria Cyciurska. Wysiłki w tym kierunku zaowocowały już sporymi sukcesami, między innymi na Konkursie Recytatorskim „Kresy” im. Adama Mickiewicza w Kijowie, z którego młodzi barczanie w każdym roku przywożą nagrody i wyróżnienia.</p>
			<p>Młodzież polska z naszego miasta uczy się również w polskich liceach i na uniwersytetach osiągając bardzo dobre wyniki. Istnieje wiele powodów dla których młodzi i starsi barczanie uczą się języka polskiego. Najczęstsze z nich to: chęć poznania języka przodków, potrzeba otrzymania Karty Polaka oraz chęć podjęcia nauki lub pracy w Polsce.</p>
			<p>Dodatkowym bodźcem do nauki dla młodzieży i dzieci jest chęć przynależności do jednego z działających przy Domu Polskim w Barze zespołów, czyli do chóru „Młode Liście” lub grupy tanecznej „Aksamitki”. Zespoły te prezentują bardzo wysoki poziom artystyczny i dlatego często zapraszane są na gościnne występy do Polski, między innymi do Kwidzyna, Wrocławia, Mrągowa, Rybnika, Rzeszowa, Kielc. Równocześnie miasta partnerskie Baru (Kwidzyn, Rybnik i Starachowice) w każdym roku przygotowują dla członków zespołów atrakcyjny letni wypoczynek.</p>
			<p>Powstałe w roku 1999 przy Barskim Rejonowym Kulturalno – Oświatowym Stowarzyszeniu Polaków im. Konfederatów Barskich zespoły artystyczne, a mianowicie: chór młodzieżowy „Młode Liście”, chór kameralny „Cantica Anima” i zespół folklorystyczno – taneczny „Aksamitki” korzystają obecnie z  profesjonalnie wyposażonych pracowni. Po latach korzystania z wynajętych, nieprzystosowanych do tego typu działalności, sal i lokali, kierownicy zespołów i ich wychowankowie mają nareszcie komfortowe warunki pracy.</p>
